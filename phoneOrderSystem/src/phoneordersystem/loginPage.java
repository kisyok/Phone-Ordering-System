    private void loginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginButtonMouseClicked
        if(checkUsernameMatch(username.getText())==true){
            try{
            Scanner s = new Scanner(new FileInputStream("loginDatabase.txt"));
            while(s.hasNextLine()){
                String str = s.nextLine();
                if(str.equals(username.getText())){
                    String next = s.nextLine();
                    if(next.equals(password.getText())){
                        this.setVisible(false);
                        orderSystem a = new orderSystem();
                        a.setVisible(true);
                    }else{
                        error.setText("Invalid password");
                    }
                }
            }
            s.close();
        }catch(FileNotFoundException e){
            System.out.println("Fi;e not found");
        }
        }else{
            error.setText("Invalid username");
        }
        
        
    }//GEN-LAST:event_loginButtonMouseClicked
    public boolean checkUsernameMatch(String a){
        boolean match = false;
        try{
            Scanner s = new Scanner(new FileInputStream("loginDatabase.txt"));
            while(s.hasNextLine()){
                String str = s.nextLine();
                if(str.equals(a)){
                    match = true;
                    break;
                }
            }
            s.close();
        }catch(FileNotFoundException e){
            System.out.println("Fi;e not found");
        }
        return match;
    }
