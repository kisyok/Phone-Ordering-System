    public boolean foundNumber(String a){
        char[] arr = a.toCharArray();
        boolean num = false;
        for(int i=0; i<arr.length; i++){
            if(Character.isDigit(arr[i])){
                num = true;
                break;
            }
        }
        return num;
    }
    public boolean foundLowercase(String a){
        char[] arr = a.toCharArray();
        boolean lower = false;
        for(int i=0; i<arr.length; i++){
            if(Character.isLowerCase(arr[i])){
                lower = true;
                break;
            }
        }
        return lower;
    }
    public boolean foundUppercase(String a){
        char[] arr = a.toCharArray();
        boolean upper = false;
        for(int i=0; i<arr.length; i++){
            if(Character.isUpperCase(arr[i])){
                upper = true;
                break;
            }
        }
        return upper;
    }
