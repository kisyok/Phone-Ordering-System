    private void signupButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_signupButton2MouseClicked
        error1.setVisible(false);
        
        if(username1.getText().equals("")){
            error1.setText("Enter username");
            error1.setVisible(true);
        }else if(password1.getText().equals("")){
            error1.setText("Enter password");
            error1.setVisible(true);
        }else if(username1.getText().length()<5 || username1.getText().length()>7){
            error1.setText("Username must be between 5 to 7 characters");
            error1.setVisible(true);
        }else if(password1.getText().length()<8 || password1.getText().length()>10){
            error1.setText("Password must be between 8 to 10 characters");
            error1.setVisible(true);
        }else if(foundNumber(password1.getText()) == false){
            error1.setText("Password must consists of digits, lowercase and uppercase letter");
            error1.setVisible(true);
        }else if(foundLowercase(password1.getText()) == false){
            error1.setText("Password must consists of digits, lowercase and uppercase letter");
            error1.setVisible(true);
        }else if(foundUppercase(password1.getText()) == false){
            error1.setText("Password must consists of digits, lowercase and uppercase letter");
            error1.setVisible(true);
        }else if(checkUsernameMatch(username1.getText())){
            error1.setText("This username already existed");
            error1.setVisible(true);
        }else{
            try{
                PrintWriter p = new PrintWriter(new FileOutputStream("loginDatabase.txt",true));
                p.println(username1.getText());
                p.println(password1.getText());
                p.close();
            }catch(IOException e){
                System.out.println("Problem with file output");
            }
            this.setVisible(false);
            orderSystem a = new orderSystem();
            a.setVisible(true);
        }
    }//GEN-LAST:event_signupButton2MouseClicked

