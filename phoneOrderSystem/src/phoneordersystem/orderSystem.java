    public boolean specialCharacters(String a){
        String special = "!@#$%&*()'+,-./:;<=>?[]^_`{|}";
        boolean found = false;
        for(int i=0; i<a.length(); i++){
            char ch = a.charAt(i);
            if(special.contains(Character.toString(ch))){
                found = true;
                break;
            }
        }
        return found;
    }
